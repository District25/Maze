void Maze::subscribe(Observer* o) {
    observers.push_back(o);  // Ajouter un observer Ã  la liste
}

void Maze::notifyObservers() {
    for (Observer* o : observers) {
        o->update();  // Appeler update() sur chaque observer
    }
}

